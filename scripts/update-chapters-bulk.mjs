「所以有些連結發生了，但我不知道，」我說。

「比你以為的更常，」她說，「你睡著的時候，你的靈魂和守護靈之間的交流，比你清醒時能感知到的，多得多。那些你醒來不記得的，也許是最安靜、最日常的那種連結，不需要你知道，就只是——同在，陪著，在你睡著的時候輕輕地靠近，然後在你要醒來的時候，退回到那個你清醒時能感覺到的距離。」

「那，」我說，「為什麼偏偏在某一個時刻夢見他們，而不是別的時刻？那個時機，是隨機的嗎？」

靈媒阿姨把茶杯放下，搖了搖頭，「不是隨機的。」

「那是什麼決定的？」

「兩件事，」她說，「第一件，是你的狀態。你那個晚上的頻道，夠不夠開，雜訊夠不夠少，讓那個接觸可以發生，而且你醒來之後能記得。這也是為什麼，有時候你非常想夢見一個人，偏偏夢不到——因為想念本身製造了雜訊，讓頻道反而關起來了。反倒是某些你沒有特別在想的夜晚，頻道安靜地開著，那個夢就來了。」

「第二件，」她繼續，「是那個靈魂這個時候的狀態，以及它判斷這個時刻需要傳遞什麼給你。守護靈選擇透過夢境傳遞訊號，通常是因為這個訊號，用清醒時的方式傳遞不夠完整，或者你清醒時的雜訊太大，但睡著的頻道開著，那就是最好的時機。」

「所以那個時機，是它選的，」我說，「不是我選的。」

「不是你選的，」她說，「但你可以創造那個條件——讓自己在那個晚上的頻道開著，讓自己睡前的狀態安靜一點，讓那個可能性高一些。」

「睡前怎麼讓頻道開著？」我問。

「睡前不看讓你大腦激動的東西，」她說，「不看會讓你焦慮的新聞，不做會讓你心跳加快的事。讓自己慢下來，讓呼吸慢下來，讓身體知道現在可以放鬆了。有時候，在睡前輕輕地對那個人說一句話，就只是說，今晚，我想見你——不是要求，不是渴望，就是輕輕地說，然後放下那個說，睡著。」

「輕輕地說，然後放下，」我說，「不是抓著那個願望入睡。」

「放下，」她說，「才是那個邀請真正被送出去的時候。」

「那在夢裡，」我說，「他們說的話，做的事，都有意義嗎？還是有些只是我大腦的填充？」

「這個問題，」她說，「需要你學會分辨兩種夢。」

「怎麼分辨？」

「第一種，是你非常想念一個人，然後你大腦替你製造了一個夢，讓那個人出現，說你想聽的話，做你希望的事，」她說，「那種夢的特徵是——一切都很符合你的期望，很像你清醒時的想像，那個人說的話，你事先就猜得到，而且那個夢有一種很用力的感覺，像是你在主導它。」

「第二種，」她說，「是真實接觸的夢。那種夢的特徵通常是——那個人說了一些你沒有預期的話，或者整個夢的場景和邏輯，是你的大腦不會製造的，有時候帶著一種你說不清楚但感覺很重要的氛圍，醒來之後那個感覺留著，而且那個夢往往比大腦的夢更清晰，更穩定，不像大腦的夢那樣跳躍、破碎。」

「你說那個人說了你沒有預期的話，」我說，「比如什麼？」

「比如，」她說，「他告訴你一件你不知道的事，然後後來你在現實裡確認了那件事是真的。比如他說了一句話，那句話用的是他在人間特有的語氣，一個你不會替他製造的語氣。比如他出現的那個場景，是你從來沒有想像過的地方，但那個地方有一種說不清楚的真實感。比如他在夢裡給你一個擁抱，那個擁抱的溫度和重量，讓你醒來之後，身體記得那個感覺。」

「那個擁抱，」我說，想起一件事，「我有一次，夢見外婆，她什麼都沒有說，就只是站在那裡，然後我走過去，她抱了我。那個抱的感覺，我醒來之後身體還記得，不是一般的夢裡的那種模糊的身體感，是非常真實的，她的手放在我背上的重量，她的肩膀的高度，她身上的氣味——那個氣味，我醒來之後還聞得到，在我的鼻子裡留了好一段時間才散。」

靈媒阿姨聽完，安靜了一下，然後說：

「那不是夢。」

我愣了一下，「那是什麼？」

「那是她來看你了，」她說，很平靜，像在說一件很日常的事，「那個氣味，是你的大腦沒有辦法製造的，你大腦裡存的是你對那個氣味的記憶，但記憶是模糊的，是隔著一層的，它沒有辦法讓你醒來之後真實地聞到那個氣味。那個氣味留在你的鼻腔裡，是因為有一個真實的頻率在你睡著的時候，非常近地靠近過你，近到留下了那個痕跡。」

「非常近地靠近過我，」我說，聲音有點啞。

「那個抱，」她說，「你記得的那個重量和溫度，也是同樣的道理。大腦記得的擁抱，是一個概念，是一個輪廓，是一種感覺的影子。你那個夢裡記得的那個具體，是真實接觸之後，留在身體裡的印記。」

那句話說出來，我沉默了很久。

想起那個夢，想起醒來之後那種身體還記得的溫度，想起那個在鼻子裡留了好一段時間的氣味，想起我當時對自己說，是夢，只是一個很真實的夢。

但也許，那不只是夢。

也許那個夜晚，她真的來了，真的抱了我，真的讓我的身體記住那個她還在的溫度，然後在我快要醒來之前，輕輕地退開了，讓我以為那是一個夢，讓我的頭腦可以接受那件事，讓我不會醒來之後因為那個真實而更加難以承受她不在的現實。

「她讓我以為是夢，」我說，「是因為怕我接受不了？」

「是因為，」靈媒阿姨說，「以那個形式傳遞，你最能接收，而且那個接收對你最溫柔。讓你醒來，帶著那個溫度，帶著那個氣味，帶著一種說不清楚但真實的被陪過的感覺，不需要解釋，不需要你相信什麼，那個感覺本身，就夠了。」

「那個感覺本身，就夠了，」我說，把那句話放在心裡，讓它沉下去，沉到一個很深的地方，和那個身體還記得的溫度，放在一起。

然後我想到一件事——

外婆身上的氣味。

那個氣味我一直記得，但我以為我只是在記憶裡記得，以為那個記憶的版本是模糊的，是隔著一層玻璃的。

但那個夢裡，那個氣味是清晰的，是真實的，是讓我醒來之後還在鼻子裡的。

她帶著那個氣味來了。

「阿姨，」我說，「氣味，是靈魂最常用的通訊方式嗎？不只是在夢裡，是清醒的時候也會用？」

靈媒阿姨的眼神裡有一種我說不清楚的東西，像是這個問題讓她想到了什麼，「你問到下一章了，」她說，帶著一點點笑，「但你已經感覺到了，對不對？」

「感覺到什麼？」

「感覺到，那個夢裡的氣味，和你清醒時偶爾忽然聞到的那個氣味，可能是同一個人，用同一種方式，只是在不同的狀態下傳遞給你，」她說，「你有沒有，在清醒的時候，忽然聞到一個不應該在那個地方出現的氣味，然後你想起了某一個人？」

我的心跳慢了半拍。

「有，」我說，「不只一次。」

「那，」她說，「我們下次，好好說氣味的事。因為氣味，是所有通訊方式裡，最直接抵達你的身體的一種，也是靈魂最喜歡用的一種——不是因為它方便，是因為它繞過了你的頭腦，直接落在你最古老的那個感知裡。」

「最古老的感知，」我說。

「氣味，」她說，「是人類最古老的感官，比語言古老，比思考古老，在你的身體裡，它走的路最短，到達的地方最深。靈魂知道這件事，所以它選擇那條路。」

那天晚上我回家，在睡前，我做了靈媒阿姨說的那件事。

我在心裡輕輕地說——

「阿嬤，如果可以，今晚，我想見你。」

然後我放下那個說，讓它飄出去，不抓著它，就只是讓它去，然後閉上眼睛，讓呼吸慢下來。

那個夜晚，我睡得很深。

早上醒來，我不記得做了什麼夢。

但我的枕頭，有一個很淡很淡的、我說不清楚從哪裡來的氣味，像是某一個廚房的氣味，是油和米飯和某種香料的混合，是一個非常普通的、非常家常的氣味，但那個氣味讓我躺在床上，不想立刻起來。

我就那樣躺著，讓那個氣味在空氣裡慢慢散開。

散開之前，我在心裡說了一句：「我知道你來了。」

然後那個氣味，像是回應一樣，又淡了一點，再淡了一點，最後消失在早晨的空氣裡。

但那個被陪過的感覺，留下來了，

留了整整一天。

