那種感覺，我第一次說出口，是對小誠說的。

我們在他家吃晚飯，他煮了一鍋很普通的番茄蛋花湯，兩個人坐在餐桌前，電視開著但沒有人在看。飯吃到一半，我不知道為什麼，突然有一種很強烈的感覺——房間裡不只有我們兩個人。

不是鬼片那種感覺。不是背脊發涼，不是汗毛直豎。是一種很安靜的、很溫暖的「還有人在這裡」。像是你在一個空間裡，窗外有陽光斜斜照進來，你不需要抬頭，就知道那道光在那裡。

我把湯匙放下，環顧了一下餐桌。

「你媽媽很喜歡在廚房嗎？」我問小誠。

他抬起頭，表情有點奇怪，「對啊，她說廚房是她的地盤。你怎麼了？」

「沒有，」我說，「就是剛剛有一種感覺。」

他放下筷子，看著我，「什麼感覺？」

我想了一下，找了一個最接近的說法：「就是覺得，她剛剛好像在這裡。」

他沒有說話，只是低下頭去，過了很長一段時間，才輕輕說：「我有時候也會。」

那之後，我開始注意那種感覺。

不是刻意去找，是開始注意到它的出現和消失都有一種規律，雖然我說不清楚那規律是什麼。有時候是在很普通的瞬間——洗碗，等紅燈，在便利商店結帳——忽然有一種說不清的「在」。不是幻覺，不是我的想像，是一種類似「空氣改變了一點什麼」的感受，輕微但確實。

有時候什麼都沒有。

不是感受不到，是那個頻道完全靜默。你閉上眼睛，試著去感覺，什麼都找不到，像是試著聯絡一個一直沒有訊號的號碼，響了很久，沒有人接。

那種「什麼都沒有」的時候，有一種特別的失落。不是新的失落，是那個舊的失落，舊的那個你以為已經好一點了的傷，忽然又打開了——原來他真的不在了，原來我剛才感覺到的只是我自己想像的，原來什麼都沒有留下來。

我把這兩種感覺都說給靈媒阿姨聽，問她，那個忽有忽無，是怎麼回事？

她沒有立刻回答。她讓我把那兩種感覺說得更仔細一些——「感覺得到」的時候，我在做什麼，我的狀態是什麼；「感覺不到」的時候，又是什麼情形。

我想了很久，試著去回憶每一個細節。

「感覺得到的時候，」我說，「好像都是我沒有在想任何事的時候。就是那種，腦子放空，手上在做一件很重複的事，或者只是坐著發呆。」

「感覺不到的時候呢？」

「感覺不到的時候……」我停了一下，「通常是我壓力很大的時候，或者我刻意去找那個感覺的時候，反而找不到。」

靈媒阿姨點了點頭，像是這個答案在她預期之內。

「你剛剛說的，」她說，「就是頻率和雜訊的問題。」

「靈魂的存在，」她說，「是有頻率的。每一個靈魂的頻率不一樣，就像每一個人說話的聲音不一樣。你如果要接收到那個頻率，你自己也需要調到一個接近的頻道上。」

「像收音機，」我說。

「很像，」她說，「你要聽某一個電台，你必須把旋鈕轉到那個頻率上。轉到了，聲音清晰；轉不到，就只有雜訊。」

「那什麼是雜訊？」

她往椅背上輕輕靠了一下，「你生活裡所有會讓你的心靜不下來的東西，都是雜訊。焦慮是雜訊，憤怒是雜訊，過度的悲傷是雜訊，太多的雜念是雜訊。不是說這些情緒不對，它們都是正常的、必要的——但當這些東西同時在你的內部高速運轉，那個噪音，會蓋過你能接收的頻率。」

我想起那些「感覺不到」的時刻。幾乎都是我狀態很差的時候，失眠之後，工作出了問題之後，或者是某個突然特別想念的夜晚，越想越用力，越用力越什麼都沒有。

「所以越想找，越找不到，」我說。

「對，」她說，「因為『用力去找』這個動作本身，就是一種雜訊。那個力道，會把頻率推開。」

「那感覺得到的那些時刻呢？」我問，「那些放空的瞬間，為什麼反而能感覺到？」

「因為那個時候你的內部安靜了，」她說，「不是你變得更有靈性，不是你突然打開了什麼特別的感官，只是，你暫時放下了那些雜訊。你的頻道淨空了，訊號才能進來。」

她喝了一口茶，繼續說：「靈魂一直都在發送訊號。不是偶爾，是持續的。就像那個電台二十四小時都在播音，但你不是每一刻都能收到，取決於你的收音機此刻有多少雜訊。」

「所以那個『感覺得到他在』，不是我的想像？」

「不是，」她說，很確定地，「那是你的頻道短暫地打開了，真實地接收到了。那個感覺的質地和想像是不一樣的，你自己心裡其實知道。」

我想了一下，點了點頭。她說得對。那個感覺的質地確實不一樣。我想像出來的東西，有一種「用力」的感覺，像是我在主動建構一個畫面。但那個忽然出現的「在」，是被動的，是它自己進來的，我沒有邀請它，它就到了。

「那，」我說，「感覺不到的時候，不代表它不在？」

「不代表，」她說，「只代表那個時候，你們之間有雜訊。」

那個答案，讓我心裡某個一直繃著的東西，鬆了一點。

我沒有意識到自己一直繃著那個東西，直到它鬆開，才發現它原來一直在那裡。那是一種害怕——害怕那些感覺不到的時刻，才是真相；害怕感覺得到的那些，只是我的願望在作怪；害怕他真的走遠了，走到一個我再也觸碰不到的地方。

但如果那只是雜訊的問題——

如果它一直都在，只是我的收音機有時候太吵——

那個失去就不是完全的失去，那個連結就不是真的斷了，只是，還需要學習怎麼把頻道調得更清晰一些。

「那要怎麼降低雜訊？」我問，「有沒有什麼方法？」

靈媒阿姨看著我，眼神裡有一種我已經漸漸熟悉的、溫和的篤定。

「有，」她說，「但方法可能跟你想的不一樣。不是儀式，不是特定的時間地點，也不是你需要有什麼特別的天賦。」

「那是什麼？」

「是一種狀態，」她說，「一種你其實已經知道怎麼進入的狀態，只是你不知道那就是答案。」

我在回家的捷運上，把這些話在心裡反覆轉了很久。一種狀態。不是儀式，是狀態。我看著車窗外的黑暗，想起那個在小誠家餐桌邊的傍晚，那個忽然的「在」——那個時候我在做什麼？我在喝湯。就只是在喝湯，什麼都沒有想。也許答案，一直比我以為的簡單。

