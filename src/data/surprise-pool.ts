/**
 * 依日期種子從 pool 中選一則，同一天永遠選同一則，年份不同結果不同
 * @param dateStr YYYY-MM-DD 格式
 */
export function getPoolSurprise(dateStr: string): SurpriseItem {
  const [year, month, day] = dateStr.split('-').map(Number);
  // 計算當年第幾天
  const start = new Date(year, 0, 0);
  const date = new Date(year, month - 1, day);
  const dayOfYear = Math.floor((date.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
  // 年份也納入，讓每年相同日期選到不同內容
  const seed = (year * 7 + dayOfYear * 13) % SURPRISE_POOL.length;
  return SURPRISE_POOL[seed];
}
